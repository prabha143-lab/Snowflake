Query 1: Employees earning more than their managers

SELECT E.EMPNO, E.ENAME, E.SAL AS EMP_SAL, M.ENAME AS MANAGER_NAME, M.SAL AS MGR_SAL
FROM EMP E
JOIN EMP M ON E.MGR = M.EMPNO
WHERE E.SAL > M.SAL;


